/* .toPrecision() mengembalikan nilai numerik dengan panjang tertentu. 
Dibutuhkan argumen yang menandakan panjangnya. Jika diberikan tanpa panjang tertentu, 
metode akan mengembalikan angka sebagaimana adanya. */

// Example 1
var num = 456.789;
console.log(num.toPrecision())      
// Output : 456.789

// Example 2
var number = 456.789;
console.log(number.toPrecision(2))     
// Output : 4.6